/**
 * Report Generator
 * Generates comparison reports in various formats.
 */

import type {
  ComparisonResult,
  ComparisonReport,
  ReportFormat,
  ChangeType,
} from './types';

/**
 * Generate a comparison report
 */
export function generateReport(
  result: ComparisonResult,
  options: {
    includeVisualDiff?: boolean;
    includeThumbnails?: boolean;
  } = {}
): ComparisonReport {
  return {
    title: `Comparison: ${result.document1.name} vs ${result.document2.name}`,
    generatedAt: Date.now(),
    result,
    includeVisualDiff: options.includeVisualDiff ?? false,
    includeThumbnails: options.includeThumbnails ?? false,
  };
}

/**
 * Export report to text format
 */
export function exportToText(report: ComparisonReport): string {
  const { result } = report;
  const lines: string[] = [];

  // Header
  lines.push('=' .repeat(60));
  lines.push('DOCUMENT COMPARISON REPORT');
  lines.push('=' .repeat(60));
  lines.push('');

  // Document info
  lines.push('Documents Compared:');
  lines.push(`  Document 1: ${result.document1.name} (${result.document1.pageCount} pages)`);
  lines.push(`  Document 2: ${result.document2.name} (${result.document2.pageCount} pages)`);
  lines.push('');

  // Summary
  lines.push('-'.repeat(60));
  lines.push('SUMMARY');
  lines.push('-'.repeat(60));
  lines.push(`Overall Similarity: ${result.summary.overallSimilarity}%`);
  lines.push(`Pages Compared: ${result.summary.pagesCompared}`);
  lines.push(`Total Changes: ${result.summary.totalTextChanges}`);
  lines.push(`  - Added: ${result.summary.addedCount}`);
  lines.push(`  - Removed: ${result.summary.removedCount}`);
  lines.push(`  - Modified: ${result.summary.modifiedCount}`);
  if (result.summary.movedCount > 0) {
    lines.push(`  - Moved: ${result.summary.movedCount}`);
  }
  lines.push('');

  // Page-by-page details
  lines.push('-'.repeat(60));
  lines.push('PAGE DETAILS');
  lines.push('-'.repeat(60));

  for (const page of result.pages) {
    if (page.textChanges.length > 0) {
      lines.push('');
      lines.push(`Page ${page.pageNumber} (${page.similarity}% similar)`);
      lines.push(`  Changes: ${page.textChanges.length}`);

      for (const change of page.textChanges) {
        const symbol = getChangeSymbol(change.type);
        const text = truncateText(change.text, 50);
        lines.push(`    ${symbol} ${text}`);
      }
    }
  }

  // Footer
  lines.push('');
  lines.push('-'.repeat(60));
  lines.push(`Report generated: ${new Date(report.generatedAt).toISOString()}`);
  lines.push('Generated by PaperFlow');

  return lines.join('\n');
}

