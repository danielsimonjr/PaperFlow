/**
 * Export report to JSON format
 */
export function exportToJSON(report: ComparisonReport): string {
  return JSON.stringify(report, null, 2);
}

/**
 * Export report to specified format
 */
export function exportReport(report: ComparisonReport, format: ReportFormat): string {
  switch (format) {
    case 'text':
      return exportToText(report);
    case 'html':
      return exportToHTML(report);
    case 'json':
      return exportToJSON(report);
    case 'pdf':
      // PDF generation would require pdf-lib integration
      // For now, return HTML that can be printed to PDF
      return exportToHTML(report);
    default:
      return exportToText(report);
  }
}

/**
 * Get symbol for change type
 */
function getChangeSymbol(type: ChangeType): string {
  switch (type) {
    case 'added':
      return '+';
    case 'removed':
      return '-';
    case 'modified':
      return '~';
    case 'moved':
      return '>';
    default:
      return '?';
  }
}

/**
 * Truncate text to max length
 */
function truncateText(text: string, maxLength: number): string {
  const singleLine = text.replace(/\n/g, ' ').trim();
  if (singleLine.length <= maxLength) return singleLine;
  return singleLine.substring(0, maxLength - 3) + '...';
}

/**
 * Escape HTML special characters
 */
function escapeHtml(text: string): string {
  const map: Record<string, string> = {
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#039;',
  };
  return text.replace(/[&<>"']/g, (m) => map[m] || m);
}

/**
 * Download report as file
 */
export function downloadReport(
  report: ComparisonReport,
  format: ReportFormat,
  filename?: string
): void {
  const content = exportReport(report, format);
  const extension = format === 'text' ? 'txt' : format;
  const mimeType =
    format === 'html'
      ? 'text/html'
      : format === 'json'
        ? 'application/json'
        : 'text/plain';

  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);

  const a = document.createElement('a');
  a.href = url;
  a.download = filename || `comparison-report.${extension}`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
